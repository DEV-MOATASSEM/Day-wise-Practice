public class Switch_with_string {
    public static void main(String[] args) {
        String a="Hello";
        
        switch(a){
            case "Hi":
                System.out.println("Hello");
                break;
            case "Hello":
                System.out.println("Hi");
                break;
            default:
                System.out.println("Greetings");
        }
    }
    
}
