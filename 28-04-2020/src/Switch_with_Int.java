public class Switch_with_Int {
    public static void main(String[] args) {
        int i=9;
        switch(i)
        {
            case 0:
                System.out.println("zero");
                break;
            case 1:
                System.out.println("One");
                break;
                
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
                
            default:
                System.out.println("Some Number");
        }
    }
    
}
