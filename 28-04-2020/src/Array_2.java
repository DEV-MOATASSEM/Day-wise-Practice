public class Array_2 {
    public static void main(String[] args) {
        int d[][]={
            {1,2,3,4,6},
            {9,8,7,4},
            {5,6,7,4,7,3}
        };
        for (int i=0;i<d.length;i++){
            for (int j=0;j<d[i].length;j++){
                System.out.print(" "+d[i][j]);
            }
        System.out.println();
        }
        
    }
    
}
