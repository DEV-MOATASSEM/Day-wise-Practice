public class Ternary_Operator {
    public static void main(String[]args){
        //if the condition is true expression_1 will be executed otherwise expression_2 will be executed
        // In if_else roem
        
        int i=4;
        int j;
        /*
        if (i>6)
            j=1;
        else
            j=2;
        */
        //In ternary operator form
        j=i>6?1:2;
        System.out.println(j);
    }
    
}
